 <h2>Get to know LocalAway</h2>
 <p>LocalAway is a unique marketplace for fashion boutiques with a distributed warehouse model. We don’t own stock but provide a platform to sell personalized style to a large #shopsmall #shoplocal audience.  On localaway.com, you’ll find a community of boutiques, stylists and curators, who all share a passion for local styles. 
 If you’re new to LocalAway, take some time to get to know us. Review our terms & conditions for boutiques on LocalAway to make sure that selling on LocalAway is right for you and your business.<p>
 <h2>Open your shop</h2>
 <p>Before you create your shop, sign in or create a LocalAway account.  You’ll use this account to sell your products and to create personalized box of styles. After creating your account, add your profile picture, bio and products to let other people on LocalAway know who you are.<p>
<p>To open your LocalAway account:</p>
 <p>1. Click <strong>Partner with Us</strong> at the top right of LocalAway.</p>
 <p>2. Fill out the <strong>Application</strong>.</p>
 <p>3. LocalAway verifies your Application and will send you a <strong>confirmation e-mail</strong> with your login</p>
 <p>4. <strong>Login</strong> to your personal shop site </p>
 <p>5. Click on <strong>MyShop</strong> </p>
 <p>6. Upload your <strong>logo, photo, description</strong> and all the items of your <strong>closet</strong>.</p>
 <h3>Do I need a business license to sell on LocalAway?</h3>
 <p>LocalAway doesn’t require sellers to have a business license to sell on LocalAway.
 However, when selling on LocalAway you're required to follow any laws that apply to you as a small business selling online. If you have any questions regarding which laws apply to you, please contact a qualified professional.<p>
 <h2>Finish your shop setup</h2>
 <p>Once you’ve signed up to sell on LocalAway, there are a few steps you need to take to complete your shop setup and prepare to start selling items. Like any online business, you should think through key factors in your selling workflow. Consider things like production time, policies, and your plan for shipping. </p>
 <p>Key shop elements to address:</p>
 <li><strong>Banner and shop logo:</strong> Upload clear photos that represent the style of your brand and make a strong first impression on shoppers. </li>
 <li><strong>Shop announcement:</strong> Share a brief welcome message with shoppers. </li>
 <li><strong>About section:</strong> Upload videos or photos and share the story behind your products, how your business came to be, and what your vision is for your brand. </li>
 <li><strong>Shop members:</strong> Introduce any other members of your team or people you collaborate with. </li>
 <li><strong>Shop policies:</strong> Outline all your policies for shipping, returns, exchanges, payments, custom items, or any other guidelines that are important for shoppers to know before they make a purchase.</li>
 <p>Our shop managers are happy to support you on all these efforts. Please reach out to hello@localaway.com.</p>
 <h2>Add listings to your shop and start marketing</h2>
 <p>Once you’ve completed all your shop sections, you are ready to start adding products to your LocalAway shop. </p>
 <p>Marketing is key to the success of your LocalAway shop. Marketing will help new customers find your products, keep your existing customers up to date with the latest news and products, and can lead to an overall rise in sales. </p>
 <p>You can reach out to our marketing experts (hello@localaway.com) to get tips for getting started with marketing:
 <li>Make sure you’re optimizing your shop for LocalAway search. Make sure to add your location so that your shop can be found in local searches. </li>
 <li>Link your shop to your social media accounts so you can easily create posts about your business. </li>
 <li>Consider marketing your shop through paid advertising.</li>
 <li>Consider offering discounts to incentivize shoppers to buy with sales and coupons.  </li>
 <li>Connect your local community to your online LocalAway presence. </li>
 <h2>Manage your shop</h2>
 <p>After you’ve listed your items in your shop and created some boxes of style, there are other factors to manage as your business develops and grows.</p>
 <p>Tips for managing your shop:</p>
 <li>Make sure you follow customer service best practices (conversation, consulting, payment, shipment, return, refund, customer satisfaction) </li>
 <li>Be nice to every customer</li>
 <li>Reach out to us in case you have questions and feedback (hello@localaway.com)</li>
 <li>Join our #localaway movement and support #shopsmall #supportsmallbusiness</li>
 <li>Take a break from your shop when needed.</li>
 <li>Have fun! :)</li>
 <h2>Connect with the LocalAway community</h2>
